create table Judete
(id_judet varchar(2) constraint pk_judet primary key,
 nume_judet varchar(25) constraint vf_nume_judet_not_null not null constraint vf_nume_judet_unique unique
);

create table Producatori
(id_producator number(5) constraint pk_producator primary key,
 nume_producator varchar(40) constraint vf_producator_unic unique constraint vf_producator_not_null not null,
 website varchar(50)
);

create table Platforme
(
 id_platforma number(3) constraint pk_platforma primary key,
 nume_platforma varchar(20) constraint vf_nume_platforma_unique unique constraint vf_nume_platforma_not_null not null 
);

create table Produse
(id_produs number(5) constraint pk_produs primary key,
 id_producator number(5) constraint fk_producator_produs references Producatori(id_producator) on delete cascade constraint fk_producator_not_null not null,
 nume_produs varchar(100) constraint vf_nume_produs not null,
 pret number(5) constraint vf_pret check(pret>-1),
 cantitate number(4) constraint vf_cantitate check(cantitate>-1),
 descriere varchar(2000),
 categorie varchar(10)
);

create table Compatibilitate_Produs(
id_produs number(5) constraint fk_comp_produs references Produse(id_produs) on delete cascade constraint fk_comp_prod_not_null not null,
id_platforma number(3) constraint fk_comp_platforma references Platforme(id_platforma) on delete cascade constraint fk_comp_platforma_not_null not null,
constraint pk_comp primary key (id_produs,id_platforma)
);

create table IstoricPreturi
(id_produs number(5) constraint fk_pret_produs references Produse(id_produs) on delete cascade constraint vf_ist_pret_prod not null,
 data_inceput date constraint vf_data_inceput not null,
 pret number(5) constraint vf_pret_produs check(pret>0),
 constraint pk_pret primary key (id_produs,data_inceput) 
);

create table Utilizatori
(id_utilizator number(5) constraint pk_utilizator primary key,
 nume_utilizator varchar(15),
 prenume_utilizator varchar(15),
 email varchar(40) constraint vf_email not null,
 parola varchar(20) constraint vf_parola not null,
 data_nastere date,
 CONSTRAINT utilizator_unic UNIQUE (email)
 );

create table Comenzi(
id_comanda number(10) constraint pk_comenzi primary key,
id_utilizator number(5) constraint fk_comenzi references Utilizatori(id_utilizator) on delete set null,
id_judet varchar(2) constraint fk_judet references Judete(id_judet) on delete set null ,
oras varchar(20) constraint vf_oras not null,
strada varchar(20) constraint vf_strada not null,
numar varchar(3) constraint vf_numar not null,
cod_postal number(8) constraint vf_codpostal not null,
scara varchar(3),
bloc varchar(3),
apartament number(3),
numar_telefon varchar(20) constraint vf_nr_tel not null,
data_comanda date constraint vf_data_comanda not null
);

create table Produse_Comandate(
id_comanda number(10) constraint fk_comanda references Comenzi(id_comanda) on delete cascade constraint vf_pc_comanda not null,
id_produs number(5) constraint fk_produscomandat references Produse(id_produs) on delete cascade constraint vf_pc_produs not null,
cantitate number(3) constraint vf_numarproduse check(cantitate>0),
constraint pk_produsecomandate primary key (id_comanda,id_produs)
);

create table Comentarii(
id_utilizator number(5) constraint fk_intr_util references Utilizatori(id_utilizator) on delete cascade constraint vf_com_util not null,
id_produs number(5) constraint fk_intr_prod references Produse(id_produs) on delete cascade constraint vf_com_prod not null,
id_comentariu number(6),
data_publicare date,
constraint pk_comentariu primary key (id_utilizator,id_produs,id_comentariu),
continut_comentariu varchar(100) constraint vf_cont_comentariu not null
);


